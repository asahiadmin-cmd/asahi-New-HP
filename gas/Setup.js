// セクション区切りがあるシートのフォーマット
function formatSectionedSheet(sheet, sheetName) {
  const lastRow = sheet.getLastRow();
  const lastCol = sheet.getLastColumn();

  // セクション行（=== で始まる行）をハイライト
  const dataRange = sheet.getRange(1, 1, lastRow, lastCol);
  const values = dataRange.getValues();

  for (let i = 0; i < values.length; i++) {
    const firstCell = String(values[i][0]).trim();
    if (firstCell.startsWith('===') && firstCell.endsWith('===')) {
      // セクションヘッダー行
      const rowRange = sheet.getRange(i + 1, 1, 1, lastCol);
      rowRange.setBackground('#1e3a5f');  // ダークブルー
      rowRange.setFontColor('#ffffff');
      rowRange.setFontWeight('bold');
      rowRange.merge();
    } else if (i > 0 && String(values[i - 1][0]).trim().startsWith('===')) {
      // データヘッダー行（セクションの直後）
      const rowRange = sheet.getRange(i + 1, 1, 1, lastCol);
      rowRange.setBackground('#f97316');  // オレンジ
      rowRange.setFontColor('#ffffff');
      rowRange.setFontWeight('bold');
    }
  }

  // 列幅の自動調整
  for (let i = 1; i <= lastCol; i++) {
    sheet.autoResizeColumn(i);
  }

  // 固定行
  sheet.setFrozenRows(1);
}

// シンプルなシート（セクション無し）のフォーマット
function formatSimpleSheet(sheet, numColumns) {
  // ヘッダー行のフォーマット
  const headerRange = sheet.getRange(1, 1, 1, numColumns);
  headerRange.setBackground('#f97316');  // オレンジ
  headerRange.setFontColor('#ffffff');
  headerRange.setFontWeight('bold');
  headerRange.setHorizontalAlignment('center');

  // 列幅の自動調整
  for (let i = 1; i <= numColumns; i++) {
    sheet.autoResizeColumn(i);
  }

  // 枠線を追加
  const dataRange = sheet.getRange(1, 1, sheet.getLastRow(), numColumns);
  dataRange.setBorder(true, true, true, true, true, true);

  // 固定行（ヘッダー）
  sheet.setFrozenRows(1);
}

