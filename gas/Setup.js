/**
 * 「フォーム管理」シートを作成・初期化する
 * メニューから手動で実行するか、フォーム送信時に自動作成される
 */
function setupFormManagementSheet() {
  const ss = SpreadsheetApp.getActiveSpreadsheet();
  let sheet = ss.getSheetByName('フォーム管理');

  // シートがなければ作成
  if (!sheet) {
    sheet = ss.insertSheet('フォーム管理');
  } else {
    // 既存シートをクリアして再セットアップ
    sheet.clear();
  }

  // ヘッダー行を設定
  const headers = [
    '日時',
    'フォーム名',
    'お名前',
    'メールアドレス',
    '電話番号',
    '連絡希望',
    '希望職種',
    '面接形式',
    'メッセージ'
  ];

  sheet.appendRow(headers);

  // ヘッダー行のスタイル設定
  const headerRange = sheet.getRange(1, 1, 1, headers.length);
  headerRange.setBackground('#F97316');  // オレンジ
  headerRange.setFontColor('#FFFFFF');
  headerRange.setFontWeight('bold');
  headerRange.setHorizontalAlignment('center');

  // 列幅の設定
  sheet.setColumnWidth(1, 150);  // 日時
  sheet.setColumnWidth(2, 180);  // フォーム名
  sheet.setColumnWidth(3, 120);  // お名前
  sheet.setColumnWidth(4, 200);  // メールアドレス
  sheet.setColumnWidth(5, 130);  // 電話番号
  sheet.setColumnWidth(6, 100);  // 連絡希望
  sheet.setColumnWidth(7, 100);  // 希望職種
  sheet.setColumnWidth(8, 150);  // 面接形式
  sheet.setColumnWidth(9, 400);  // メッセージ

  // 固定行
  sheet.setFrozenRows(1);

  // 枠線
  headerRange.setBorder(true, true, true, true, true, true);

  // 交互の背景色設定（読みやすさ向上）
  // データが入ったら自動適用されるようにシート設定
  const bandings = sheet.getBandings();
  for (const banding of bandings) {
    banding.remove();
  }

  SpreadsheetApp.getUi().alert(
    'フォーム管理シート設定完了',
    '「フォーム管理」シートが作成されました。\n\n' +
    '【記録される項目】\n' +
    '・日時\n' +
    '・フォーム名（面接・見学 応募フォーム / お問い合わせフォーム）\n' +
    '・お名前\n' +
    '・メールアドレス\n' +
    '・電話番号\n' +
    '・連絡希望\n' +
    '・希望職種（応募フォームのみ）\n' +
    '・面接形式（応募フォームのみ）\n' +
    '・メッセージ（問い合わせフォームのみ）\n\n' +
    'フォーム送信があると自動的にこのシートに記録されます。',
    SpreadsheetApp.getUi().ButtonSet.OK
  );
}
