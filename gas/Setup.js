/**
 * スプレッドシートの初期セットアップ（方式B: 6シート構成）
 * HPのページ構成に合わせた6つのシートを作成し、テンプレートデータを挿入
 * セクションは「=== セクション名 ===」形式で区切る
 */

function setupSpreadsheet() {
  const ss = SpreadsheetApp.getActiveSpreadsheet();

  // 6つのシートを作成
  createTopPageSheet(ss);      // トップページ（ニュース + スライダー）
  createCompanySheet(ss);      // 会社概要（会社情報 + 沿革）
  createStaffSheet(ss);        // 社員紹介
  createFacilitiesSheet(ss);   // 事業所一覧
  createRecruitSheet(ss);      // 求人
  createContactSheet(ss);      // お問い合わせ

  // 最初のデフォルトシート（Sheet1）を削除
  const sheet1 = ss.getSheetByName('Sheet1');
  if (sheet1) {
    ss.deleteSheet(sheet1);
  }
  const sheet1ja = ss.getSheetByName('シート1');
  if (sheet1ja) {
    ss.deleteSheet(sheet1ja);
  }

  SpreadsheetApp.getUi().alert('セットアップが完了しました！\n6つのシートが作成されました。\n\n【シート一覧】\n1. トップページ\n2. 会社概要\n3. 社員紹介\n4. 事業所一覧\n5. 求人\n6. お問い合わせ');
}

